// Function to get translated text
function t(key, lang = getCurrentLanguage()) {
    return translations[lang]?.[key] || translations['en']?.[key] || key;
}

// Get current language from localStorage
function getCurrentLanguage() {
    return localStorage.getItem('language') || 'en';
}

// Function to update all translatable content
function updatePageLanguage(language) {
    // Update simple text elements using data-translate attributes
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.textContent = t(key, language);
    });

    // Update elements with data-translate-html for HTML content
    document.querySelectorAll('[data-translate-html]').forEach(element => {
        const key = element.getAttribute('data-translate-html');
        element.innerHTML = t(key, language);
    });

    // Update html lang attribute
    document.documentElement.lang = language;

    // Mark active language button
    document.querySelectorAll('.lang-option').forEach(btn => {
        if (btn.getAttribute('data-lang') === language) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}
